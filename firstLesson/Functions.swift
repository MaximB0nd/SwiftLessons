import Foundation

func sum(firstNumber firstNumber: Int, secondNumber secondNumber: Int) -> Int{
    return firstNumber + secondNumber
}

func pow(num: Int) -> Int{
    return num * num
}

func pow3(_ num: Int) -> Int{
    return num * num * num
}




